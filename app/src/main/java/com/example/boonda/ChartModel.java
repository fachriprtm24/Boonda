package com.example.boonda;

public class ChartModel {
    int age, measure;

    public ChartModel(){}

    public ChartModel(int age, int measure) {
        this.age = age;
        this.measure = measure;
    }

    public int getAge() {
        return age;
    }

    public int getMeasure() {
        return measure;
    }
}
